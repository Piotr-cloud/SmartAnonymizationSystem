class Dlib_CNN_Cls(FacesDetector_AbstCls):
    
    @staticmethod
    def getName(): 
        return "DlibCNN"
        
    @staticmethod
    def getDescription():
        return "Dlib CNN based detector: dlib.cnn_face_detection_model_v1(modelPath). Dlib repo: https://github.com/davisking/dlib"
        
    
    def __init__(self, 
                modelFile = UserCfg_Path(name = "Model file",
                                         description = "DlibCNN model file - network parameters",
                                         path = DlibCNN_model_path),
                upsample = UserCfg_Int_Limited(
                    name = "Upsample factor",
                    description = "Upsample of image before detecting",
                    lower_limit = 1,
                    defaultValue = 1,
                    upper_limit = 320),
                ):
        
        FacesDetector_AbstCls.__init__(self)
        
        self._modelFile = self.resolveArgument(modelFile)
        
        self.upsample = self.resolveArgument(upsample)
        self.detector = dlib.cnn_face_detection_model_v1(str(self._modelFile))
    
    
    def _detect(self, nparray):
        
        detections = DetectionsMap_Cls()
        
        factor = 800 / max(nparray.shape[:2])
        
        nparray = cv2.resize(nparray, None, fx=factor, fy=factor)
        
        gray = cv2.cvtColor(nparray, cv2.COLOR_BGR2GRAY)
        
        for result in self.detector(gray, self.upsample):
            
            rectangle = result.rect
            
            left     = rectangle.left()    / nparray.shape[1]
            top      = rectangle.top()     / nparray.shape[0]
            right    = rectangle.right()   / nparray.shape[1]
            bottom   = rectangle.bottom()  / nparray.shape[0]
            
            left, top, right, bottom = np.clip((left, top, right, bottom), 0.0, 1.0)
            
            detections.addDetection(BoundingBox_Cls(class_ = FaceID,
                                                    left = left,
                                                    top = top,
                                                    right = right,
                                                    bottom = bottom)
                                    )
        
        return detections


